// JavaScript é uma linguagem de tipagem fraca,
// ou seja, não é preciso declarar o tipo de dado utilizado em uma constante ou variável,
// não é necessário declarar que sua variável ou constante é uma string, 
// um integer ou float por exemplo

// variável do tipo string
let qualquer = "Legal"
console.log(qualquer)
console.log(typeof qualquer)

// alterando a variavel para number

qualquer = 2.15
console.log(qualquer)
console.log(typeof qualquer)

